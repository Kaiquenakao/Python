"""
1. Faça um programa que receba dois números e mostre qual deles é maior.
"""
num1 = float(input('Insira o primeiro valor:'))
num2 = float(input('Insira o segundo valor:'))
if num1 > num2:
    print(f'O primeiro valor:{num1} é maior que o segundo valor:{num2}')
else:
    print(f'O segundo valor:{num2} é maior que o primeiro valor:{num1}')