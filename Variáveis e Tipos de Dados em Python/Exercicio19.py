""""
19. Leia um valor de volume em litros e apresente-o convertido em metros cúbicos m3.
A fórmula para conversão é: M = L / 1000 sendo o L o volume em litros e M o volume em metros
cúbicos.
"""
L = float(input('Insira o seu valor em litros para converter em metros cúbicos:'))
M = L / 1000
print(f'O seu valor convertido em metros cúbicos:{M}')