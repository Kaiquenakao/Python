"""
27. Leia um valor em hectares e apresente-o convertido em metros quadrados m², A fórmula
para conversão é: M = H * 10000, sendo M a área em metros quadrados e H a área em
hectares
"""
H = float(input('Insira o seu valor em hectares para converter em metros quadrados:  '))
