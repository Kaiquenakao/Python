"""
9 - Leia uma temperatura em graus Celsius e apresente-a convertida em graus Kelvin.
A fórmula de conversão é K = C + 273.15, sendo C a temperatura em Celsius e K a temperatura
em Kelvin
"""
C = float(input("Insira o seu valor em graus Celsius para converter em Graus Kelvin:"))
K = C + 273.15
print(f"A sua conversão para Graus Kelvin:{K}")
