"""
26. Leia um valor de área em metro quadrados m² e apresente-o convertido em hectares.
A fórmula para conversão é: H = M * 0.0001, Sendo M a área em metros quadrados e H a
área em hectares.
"""
M = float(input('Insira o seu valor em metros quadrados para converter em hectares:  '))
