"""
18. Leia um valor de volume em metros cúbicos m3 e apresente-o convertido em litros.
A fórmula de conversão é: L = 1000 * M, sendo L o volume em litros e M o volume em metros
cúbicos.
"""
M = float(input('Insira o valor em metros cúbicos para converter em litros:'))
L = 1000 * M 
print(f'O seu valor convertido em litros:{L}')